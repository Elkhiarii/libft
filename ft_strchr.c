char *ft_strchr(const char *str, int c)
{
    char ft_c ;
    ft_c = (const char)c;
    int i;
    i = 0;
    while(str[i])
    {
        if(str[i] == ft_c)
            return((char *)&str[i]);
        i++;
    }
}
