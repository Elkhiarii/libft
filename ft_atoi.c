int	ft_atoi(const char *str)
{
	unsigned int	num;
	int	cp;
	int	i;
	num = 0;
	cp = 1;
	i = 0;
	while(str[i] == '\n' || str[i] == '\t' || str[i] == '\f'
			 || str[i] == '\v' || str[i] == '\r' || str[i] == ' ')
		i++;
	if(str[i] == '-' || str[i] == '+')
	{
		if(str[i++] == '-')
			cp = -1;
	}
	while(str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
}
